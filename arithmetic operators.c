// ARITHMETIC OPERATORS

#include <stdio.h>

int main(int argc, char const *argv[])
{
    /* code */
    int a;
    float b;
    a = 16;
    b = 8;

    printf("a + b  = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a % b = %d\n", a % b);

    return 0;
}
